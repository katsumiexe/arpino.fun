制作者です。<br>
この度はたくさんの人に遊んでいただき、本当にうれしく思います。<br>
売上より売れることに喜びを感じた、学生の頃の同人活動を思い出しました。<br>
<br>
作ったきっかけは<br>
ふとパーティカードゲームが作りたくなった。<br>
バランス確認で簡単なプログラムを組んでみた。<br>
イラスト入れたら動きを付けたくなってきた。<br>
動きが付いたらなんだか楽しくなってきた。<br>
レスポコーディング。<br>
みたいな流れです。<br>
<br>
システムは<br>
jQuery<br>
PHP<br>
MySQL<br>
の、いわゆる「ワードプレス3点セット」です<br>
<br><br>
さらにありがたいことに、いくつか提案も頂けました。<br>
自分の発想にはなかったもので、非常に面白いなと感じました。<br>
<br>
・アイテムの価値が途中で逆転する<br>
大富豪の『革命』ですね。これは採用。魅力が流れたらトリガーで入れてみようと思います。<br>
<br>
・パス、2枚出しができるようにする<br>
可能性のあるルールだとは思いますが、-2でパスするだけですので、何か縛りを入れんとです。<br>
2枚出しはできるがパスはできない（最後が自動的にパス状態になる）、もしくはパスした次は強制2枚出しとか。<br>
前者だと全員1回以上パスすると、最後のカードが流れてしまうのですよね。<br>
<br>
・各ユニットに性格をつけて戦略に反映させる。<br>
これは面白いと思う！プレイヤーと対戦しているように感じられるでしょう。<br>
......でもめちゃくちゃ難しいんですががが。<br>
<br>
